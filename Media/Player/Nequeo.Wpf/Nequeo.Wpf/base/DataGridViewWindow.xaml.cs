namespace Nequeo.Wpf
{
    /// <summary>
    /// Interaction logic for DataGridViewWindow.xaml
    /// </summary>
    public partial class DataGridViewWindow : Window
    {
        /// <summary>
        /// Default constructor.
        /// </summary>
        public DataGridViewWindow()
        {
            InitializeComponent();
        }

        private object _data = null;

        /// <summary>
        /// Gets or sets the data object to load.
        /// </summary>
        public object Data
        {
            get { return _data; }
            set { _data = value; }
        }

        /// <summary>
        /// Closing.
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void Window_Closing(object sender, System.ComponentModel.CancelEventArgs e)
        {
            _data = dataGrid.DataContext;
        }

        /// <summary>
        /// Loading.
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void Window_Loaded(object sender, RoutedEventArgs e)
        {
            dataGrid.DataContext = _data;
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void btnCancel_Click(object sender, RoutedEventArgs e)
        {
            this.Close();
        }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void btnApply_Click(object sender, RoutedEventArgs e)
        {
            this.Close();
        }
    }
}
